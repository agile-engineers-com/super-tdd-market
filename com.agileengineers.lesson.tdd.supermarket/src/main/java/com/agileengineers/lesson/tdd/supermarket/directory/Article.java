package com.agileengineers.lesson.tdd.supermarket.directory;

public class Article {

	private String name;

	private double price;

	private int vatClass;

	public String
	getName() {
		return name;
	}

	public void
	setName(String name) {
		this.name = name;
	}

	public double
	getPrice() {
		return price;
	}

	public void
	setPrice(double price) {
		this.price = price;
	}

	public int
	getVatClass() {
		return vatClass;
	}

	public void
	setVatClass(int vatClass) {
		this.vatClass = vatClass;
	}

}
